package Modelo;

/**
 *
 * @author josei
 */
public class Pedido {
    private int id_pedido;
    private int id_producto;
    private String nombre;
    private float precioC;
    private String categoria;
    private String marca;
    private int stock;
    private int limite;
    private String fecha;
    private String estado;
    
    public Pedido(){}
    
    public Pedido(int id_pedido,int id_producto,String nombre,float precioC,String categoria,String marca,int stock,int limite,
            String fecha,String estado){
        this.id_pedido = id_pedido;
        this.id_producto = id_producto;
        this.nombre = nombre;
        this.precioC = precioC;
        this.categoria = categoria;
        this.marca = marca;
        this.stock = stock;
        this.limite = limite;
        this.fecha = fecha;
        this.estado = estado;
    }

    /**
     * @return the id_pedido
     */
    public int getId_pedido() {
        return id_pedido;
    }

    /**
     * @param id_pedido the id_pedido to set
     */
    public void setId_pedido(int id_pedido) {
        this.id_pedido = id_pedido;
    }

    /**
     * @return the id_producto
     */
    public int getId_producto() {
        return id_producto;
    }

    /**
     * @param id_producto the id_producto to set
     */
    public void setId_producto(int id_producto) {
        this.id_producto = id_producto;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the stock
     */
    public int getStock() {
        return stock;
    }

    /**
     * @param stock the stock to set
     */
    public void setStock(int stock) {
        this.stock = stock;
    }

    /**
     * @return the limite
     */
    public int getLimite() {
        return limite;
    }

    /**
     * @param limite the limite to set
     */
    public void setLimite(int limite) {
        this.limite = limite;
    }

    /**
     * @return the fecha
     */
    public String getFecha() {
        return fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    /**
     * @return the precioC
     */
    public float getPrecioC() {
        return precioC;
    }

    /**
     * @param precioC the precioC to set
     */
    public void setPrecioC(float precioC) {
        this.precioC = precioC;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
