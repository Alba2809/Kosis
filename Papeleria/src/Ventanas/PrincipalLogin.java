    private void nombreUsuarioMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nombreUsuarioMousePressed
        //Este tipo de evento es para visualizar un texto en los campos cuando 
        //no tienen nada escrito
        if(nombreUsuario.getText().equals("Ingrese nombre de usuario")){
            nombreUsuario.setText("");
            nombreUsuario.setForeground(Color.black);
        }
        if(String.valueOf(passwordUsuario.getPassword()).isEmpty()){
            passwordUsuario.setText("********");
            passwordUsuario.setForeground(new java.awt.Color(204,204,204));
        }
    }//GEN-LAST:event_nombreUsuarioMousePressed

    private void passwordUsuarioMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passwordUsuarioMousePressed
        if(String.valueOf(passwordUsuario.getPassword()).equals("********")){
            passwordUsuario.setText("");
            passwordUsuario.setForeground(Color.black);
        }
        if(nombreUsuario.getText().isEmpty()){
            nombreUsuario.setForeground(new java.awt.Color(204,204,204));
            nombreUsuario.setText("Ingrese nombre de usuario");            
        }
    }//GEN-LAST:event_passwordUsuarioMousePressed

    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        String usuario = nombreUsuario.getText();
        char[] arrayC = passwordUsuario.getPassword();
        String pass = new String(arrayC);
        //se verifica que el usuario y contrase침a coincidan en la BD
        //y se obtiene el 치rea de ese usuario
        String resultado = controladorMetodos.buscarUsuario(usuario, pass);
        
        if (resultado.equals("Ventas")){ //Si el 치rea es de Ventas
            AreaVentas av = new AreaVentas();
            av.setVisible(true);
            this.dispose();
        }else{
            if(resultado.equals("Compras")){ //Si el 치rea es de Compras
                AreaCompras ac = new AreaCompras();
                ac.setVisible(true);
                this.dispose();
            }else{
                if(resultado.equals("Finanzas")){ //Si el 치rea es de Finanzas
                    AreaFinanzas af = new AreaFinanzas();
                    af.setVisible(true);
                    this.dispose();
                }if(resultado.isEmpty()){ //Si no coincide con lo anterior se muestra mensaje de error
                    AlertaWarning salir = new AlertaWarning(this, true);
                    salir.mensaje.setText("Valores inv치lidos.");
                    salir.setVisible(true);
                    
                    nombreUsuario.setForeground(new java.awt.Color(204,204,204));
                    nombreUsuario.setText("Ingrese nombre de usuario");
                    passwordUsuario.setText("********");
                    passwordUsuario.setForeground(new java.awt.Color(204,204,204));
                }
            }
        }
        
        
    }//GEN-LAST:event_btnContinuarActionPerformed

